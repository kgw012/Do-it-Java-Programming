package chapter02;

public class BinaryTest {

	public static void main(String[] args) {
		int num = 10;
		int twoNum = 0b1010;
		int eightNum = 012;
		int sixteenNum = 0xA;
		
		System.out.println(num);
		System.out.println(twoNum);
		System.out.println(eightNum);
		System.out.println(sixteenNum);

	}

}
